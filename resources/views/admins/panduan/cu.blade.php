    <h2 class="page-header"><a href="#cu-pengenalan">Pengenalan <small>CU</small></a></h2>
    <p class="lead">
        Halaman <b>CU</b> merupakan halaman yang berisi informasi mengenai CU anda dari informasi umum seperti alamat, kontak dan badan hukum, sampai pada visi misi serta semangat CU anda.
    </p>
    {{ Html::image($imagepath.'cu-all.png','cu', array('class' => 'img-responsive ','style'=>'margin-bottom:10px;')) }}
    <p class="lead">
        Inilah halaman <b>Laporan CU</b>. Pada halaman ini terdapat beberapa komponen yaitu:
        <ol>
            <li><b>Informasi Umum</b>: menampilkan informasi umum dari CU seperti foto kantor pusat, no.Ba, alamat, kontak dan sebagainya.</li>
            <li><b>Profil CU</b>: menampilkan profil dari CU anda. Apabila belum pernah mengisi maka akan otomatis muncul kotak untuk mengisi/menuliskan profil CU. Isi dari profil bisa berisi visi, misi, dan slogan, serta bisa juga berisi sejarah atau filosofi. Pada dasarnya profil CU adalah segala sesuatu yang ingin ditunjukkan CU kepada anggota dan masyarakat agar lebih mengenal CU anda.</li>
            <li><b>Ubah</b>: tombol yang berfungsi untuk melakukan pengubahan seluruh isi informasi pada halaman ini.</li>
            <li><b>Panel TP</b>: menampilkan jumlah Tempat Pelayanan (TP) atau Kantor Pelayanan (KP) atau cabang atau sejenisnya.</li>
        </ol>
    </p>
</div>  
